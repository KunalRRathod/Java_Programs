class IfElse
 { 
   public static void main (String args[])
    {
      int a=20;
      if (a>10)
       {
        System.out.println("No. is greater");
       }
      else if (a<10)
       { 
        System.out.println("No. is Smaller");
       }
      else
       {
        System.out.println("Nos. are equal");
       }
    }
 }